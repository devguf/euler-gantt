const BASE_COLORS = ["purple", "deepPurple", "indigo", "blue", "lightBlue", "cyan", "teal", "green", "lightGreen"];
const cache = {};
const Colors = {
  get(id = "", degree) {
    if (id === "") {
      return "";
    }
    let isSubItem = false;
    let colorId = id;
    if (id.indexOf("_") !== -1) {
      colorId = id.substring(0, id.indexOf("_"));
      isSubItem = true;
    }
    if (!cache[colorId]) {
      cache[colorId] = BASE_COLORS[parseInt(Math.random() * BASE_COLORS.length)];
    }
    if (degree) {
      return MATERIAL_COLOR[cache[colorId] + degree];
    }
    if (isSubItem) {
      return MATERIAL_COLOR[`${cache[colorId]}300`];
    }
    return MATERIAL_COLOR[`${cache[colorId]}500`];
  }
};
export default Colors;
